from util import get_directory_modules_objects

SETUP_DIR = "setup"

def setup():
    """Call all setup modules"""
    setup_objects = get_directory_modules_objects(SETUP_DIR)
    for setup in setup_objects:
        setup.setup()

if __name__ == "__main__":
    setup()
